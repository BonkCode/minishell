#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	void *bytes;

	bytes = malloc(count * size);
	if (bytes == NULL)
		return (bytes);
	ft_bzero(bytes, size * count);
	return (bytes);
}
