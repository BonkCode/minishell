#include "libft.h"

void	*ft_memset(void *dest, int c, size_t n)
{
	unsigned char	*list;

	if (!dest)
		return (NULL);
	list = (unsigned char*)dest;
	while (n-- > 0)
		*(list++) = (unsigned char)c;
	return (dest);
}
